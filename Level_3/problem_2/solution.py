from collections import deque

def solution(map):
    def bfs(x, y, map):
        graph = [[None for i in range(c)] for j in range(r)]
        q = deque([(x, y)])
        graph[x][y] = 1
        while q:
            x1, y1 = q.popleft()
            for i, j in [(0, 1), (1, 0), (0, -1), (-1, 0)]:
                x2, y2 = x1 + i, y1 + j
                if 0 <= x2 < r and 0 <= y2 < c and graph[x2][y2] is None:
                    graph[x2][y2] = graph[x1][y1] + 1
                    if map[x2][y2] != 1:
                        q.append((x2, y2))
        return graph

    r = len(map)
    c = len(map[0])

    start_graph = bfs(0, 0, map)
    end_graph = bfs(r - 1, c - 1, map)
    result = float('inf')
    for i in range(r):
        for j in range(c):
            result = min(result, start_graph[i][j] + end_graph[i][j] - 1 if start_graph[i][j] and end_graph[i][j] else float('inf'))

    return result


